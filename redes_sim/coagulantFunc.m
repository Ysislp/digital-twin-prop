function [dosis] = coagulantFunc (Ti, Ci)

    a = 76.44 - 0.08533.*Ti - 0.003054.*Ci + 0.004229.*(Ti).^2 ...
             - 0.002185.*Ti.*Ci + 0.0002715.*(Ci).^2 - 3.174e-06.*(Ti).^2.*Ci ... 
             + 1.423e-06.*Ti.*(Ci).^2 - 1.513e-07.*(Ci).^3;
        
    x = Ti;
    y = Ci;

    p00 =       95.72;
    p10 =       -1.33;
    p01 =    -0.07005;
    p20 =     0.04464;
    p11 =    -0.01072;
    p02 =    0.001403;
    p30 =  -0.0004819;
    p21 =   0.0002204;
    p12 =  -2.532e-05;
    p40 =   9.191e-07;
    p31 =  -4.632642e-07;
    p22 =   5.263e-08;

    b = p00 + p10.*x + p01.*y + p20.*x.^2 + p11.*x.*y + p02.*y.^2 + p30.*x.^3 ... 
             + p21.*x.^2.*y + p12.*x.*y.^2 + p40.*x.^4 + p31.*x.^3.*y + p22.*x.^2.*y.^2;
         
    dosis = (a + b)/2;
        
end